metric: Churn Rate
owner: Customer Success
last_updated: 2026-06-15
definition: >
  (Number of active customers whose subscription status changed to
  'cancelled' during the period AND was not reactivated within 30
  days of cancellation) / (Number of active customers at the start
  of the period). Period = trailing calendar month.
change_note: >
  On 2026-06-15, tightened the reactivation window from 30 days to
  14 days, since customers who came back after 30 days were behaving
  more like new customers than genuine saves. All dashboards using
  the old 30-day window should be updated to reflect this.
